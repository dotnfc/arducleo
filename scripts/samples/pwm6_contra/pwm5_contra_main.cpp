#include "mbed.h"

int tonePin = 13;   // dummy
PwmOut speaker(PB_1);
DigitalOut Led1 (LED1);
void midi();

// midi from: http://www.khinsider.com/midi/nes

/*==============================================================================
 * \brief arduino like 'tone ()'
 */
void tone(int pin, int frequency, float duration) 
{
    float period;

    // calculate the period of the note in turn play
    period = 1000000 / frequency;
    // Activate the LED of the core to indicate that we are playing a note
    Led1 = 1;
    // set the period calculated at the PWM output
    speaker.period_us (period);
    
    // establish a duty cycle of 50%
    speaker.write (0.50f);
    // hope the time set by the tone i
    wait_ms (duration);
    // disabled the core led to indicate the end of the note
    Led1 = 0;
    // ended the note
    speaker = 0;
}

/*==============================================================================
 * \brief arduino like 'delay (ms)'
 */
void delay (float ms)
{
    wait(ms / 1000);
}

/*==============================================================================
 * \brief main entry
 */
int main() 
{
    while (1) {
        // Play midi
        midi();
    }
}

